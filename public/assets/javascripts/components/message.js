/* ------------------------------------------------------------------------------------------------------------------ */
/* JS - Flash message button */
/* ------------------------------------------------------------------------------------------------------------------ */
const flashMessageButton = document.querySelector('#flashMessageButton');
/* ------------------------------------------------------------------------------------------------------------------ */
/* JS - Flash message container */
/* ------------------------------------------------------------------------------------------------------------------ */
const flashMessage = document.querySelector('.flashMessage');
/* ------------------------------------------------------------------------------------------------------------------ */
/* JS - Event listeners */
/* ------------------------------------------------------------------------------------------------------------------ */
if(flashMessage) {
    flashMessageButton.addEventListener("click", function() {
        flashMessage.classList.add("close");
        setTimeout(function() {
            flashMessage.remove();
        }, 550);
    });
}